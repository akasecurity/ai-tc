// Regenerates src/bundled-packs.generated.ts from the on-disk rule registry
// (repo-root rules/<pack>/manifest.json + the rule files each manifest declares),
// so the plugin's bundled detection inventory can never drift from the packs by
// hand. Every rule the manifest lists is inlined at build time (tsup bundles the
// JSON via noExternal), so detection works with zero network and zero install.
// Run: pnpm --filter @akasecurity/plugin-sdk gen:bundled-packs
//
// The companion rule-packs.test.ts re-reads the same rules/ tree and fails CI
// if the committed constant diverges (or a rule stops parsing), so a forgotten
// regenerate is caught.
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { format, resolveConfig } from 'prettier';

const here = dirname(fileURLToPath(import.meta.url));
const rulesDir = join(here, '..', '..', '..', 'rules');
const outFile = join(here, '..', 'src', 'bundled-packs.generated.ts');
// The relative specifier the generated file imports each rule JSON by. The
// generated module lives at packages/plugin-sdk/src/, so ../../../rules reaches
// the repo-root rules/ tree — the same depth rule-packs.ts used by hand.
const IMPORT_PREFIX = '../../../rules';

/** camelCase an identifier from a "<pack>/<stem>" pair, e.g. code-flaws + auth-jwt → codeFlawsAuthJwt. */
function ident(pack, stem) {
  const words = `${pack}-${stem}`.split(/[^a-zA-Z0-9]+/).filter(Boolean);
  return words
    .map((w, i) => (i === 0 ? w.toLowerCase() : w[0].toUpperCase() + w.slice(1)))
    .join('');
}

// Every pack dir with a manifest, sorted by pack id for a deterministic file.
const packDirs = readdirSync(rulesDir, { withFileTypes: true })
  .filter((d) => d.isDirectory() && existsSync(join(rulesDir, d.name, 'manifest.json')))
  .map((d) => d.name)
  .sort();

const imports = []; // { id, spec }
const packs = []; // { packId, name, version, ruleIdents: [] }

for (const dir of packDirs) {
  const manifest = JSON.parse(readFileSync(join(rulesDir, dir, 'manifest.json'), 'utf8'));
  const ruleIdents = [];
  for (const stem of manifest.rules) {
    const file = join(rulesDir, dir, `${stem}.json`);
    if (!existsSync(file)) {
      throw new Error(`${dir}/manifest.json lists "${stem}" but ${dir}/${stem}.json is missing`);
    }
    const id = ident(dir, stem);
    imports.push({ id, spec: `${IMPORT_PREFIX}/${dir}/${stem}.json` });
    ruleIdents.push(id);
  }
  packs.push({ packId: manifest.id, name: manifest.name, version: manifest.version, ruleIdents });
}

// Guard against an identifier collision (would silently drop a rule import).
const seen = new Set();
for (const { id } of imports) {
  if (seen.has(id)) throw new Error(`duplicate generated identifier: ${id}`);
  seen.add(id);
}

const importLines = [...imports]
  .sort((a, b) => a.spec.localeCompare(b.spec))
  .map(({ id, spec }) => `import ${id} from '${spec}';`)
  .join('\n');

const packEntries = packs
  .map(
    (p) =>
      `  {\n    packId: ${JSON.stringify(p.packId)},\n    name: ${JSON.stringify(p.name)},\n    version: ${JSON.stringify(p.version)},\n    rawRules: [${p.ruleIdents.join(', ')}],\n  }`,
  )
  .join(',\n');

const ruleTotal = imports.length;

const file = `// The rule packs shipped with every AKA plugin — the complete on-disk registry
// (repo-root rules/), one entry per pack, every rule the pack's manifest declares.
//
// GENERATED by scripts/gen-bundled-packs.mjs — do not edit by hand. The rule JSON
// is inlined into the hook scripts at build time (tsup bundles it via noExternal),
// so detection works with zero network and zero install steps. \`packId\` is the
// manifest id (also the prefix of every rule id, e.g. \`core-pii/email\`), so it
// joins back to findings.rule_id.
//
// Regenerate after adding/removing a rule or pack under rules/:
//   pnpm --filter @akasecurity/plugin-sdk gen:bundled-packs
//
// ${String(packs.length)} packs / ${String(ruleTotal)} rules.
/* eslint-disable simple-import-sort/imports */
${importLines}

export interface BundledPack {
  packId: string;
  name: string;
  version: string;
  rawRules: unknown[];
}

export const BUNDLED_PACKS: readonly BundledPack[] = [
${packEntries},
];
`;

const prettierOptions = (await resolveConfig(outFile)) ?? {};
const formatted = await format(file, {
  ...prettierOptions,
  parser: 'typescript',
  filepath: outFile,
});
writeFileSync(outFile, formatted);
process.stdout.write(
  `wrote ${outFile} (${String(packs.length)} packs, ${String(ruleTotal)} rules: ${packs.map((p) => p.packId).join(', ')})\n`,
);
