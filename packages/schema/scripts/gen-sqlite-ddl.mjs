// Regenerates src/drizzle/sqlite-ddl.ts from the OSS local store's drizzle-kit
// SQLite migrations (drizzle/local-sqlite, generated from the tenant-free schema
// src/drizzle/local/sqlite.ts), so the plugin/CLI's committed, bundling-safe DDL
// copy can never be hand-edited into drift.
// Run: pnpm --filter @akasecurity/schema gen:sqlite-ddl
//
// The companion sqlite-ddl.test.ts re-reads the same source files and fails CI
// if the committed constant diverges, so a forgotten regenerate is caught.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { format, resolveConfig } from 'prettier';

const here = dirname(fileURLToPath(import.meta.url));
const migrationsDir = join(here, '..', 'drizzle', 'local-sqlite');
const outFile = join(here, '..', 'src', 'drizzle', 'sqlite-ddl.ts');

const journal = JSON.parse(readFileSync(join(migrationsDir, 'meta', '_journal.json'), 'utf8'));
const migrations = [...journal.entries]
  .sort((a, b) => a.idx - b.idx)
  // EOL-normalize so a CRLF checkout can never bake \r into the constant.
  .map((e) => ({
    tag: e.tag,
    sql: readFileSync(join(migrationsDir, `${e.tag}.sql`), 'utf8').replace(/\r\n/g, '\n'),
  }));

// JSON.stringify keeps the SQL byte-accurate (escapes \n, \t, quotes); prettier
// (below) then normalizes quote style so the committed file is format-stable.
const entries = migrations
  .map((m) => `  {\n    tag: ${JSON.stringify(m.tag)},\n    sql: ${JSON.stringify(m.sql)},\n  }`)
  .join(',\n');

const file = `// CANONICAL SQLite schema for the local ~/.aka/data/aka.db store.
//
// GENERATED by scripts/gen-sqlite-ddl.mjs — do not edit by hand. This is a
// committed, bundling-safe copy of the OSS local store's drizzle-kit SQLite
// migrations (packages/schema/drizzle/local-sqlite, generated from the
// tenant-free schema src/drizzle/local/sqlite.ts), so the plugin can apply the
// exact same schema with zero runtime file I/O once tsup inlines this module
// into the hook scripts. The plugin owns local migrations (it self-applies on open via
// node:sqlite, tracking applied journal tags in its plugin-local
// migration_ledger table). sqlite-ddl.test.ts re-reads the
// source .sql files and fails if this constant drifts from them.
//
// Regenerate after changing the local store's SQLite migrations:
//   pnpm --filter @akasecurity/schema gen:sqlite-ddl
//
// drizzle emits \`--> statement-breakpoint\` as SQL line-comments, so each
// migration body is exec()'d whole (DatabaseSync.exec runs every statement).

export interface SqliteMigration {
  // drizzle journal tag, e.g. "0000_initial" — also the apply order.
  tag: string;
  // Whole-file DDL; exec()'d as one batch.
  sql: string;
}

// Ordered by drizzle journal idx. The applier tracks applied entries by \`tag\`,
// not position, so an append applies incrementally and a renumbered history
// reconciles instead of replaying.
export const SQLITE_MIGRATIONS: readonly SqliteMigration[] = [
${entries},
];
`;

// resolveConfig returns null when no config is found; default to {} so the
// spread below is explicit about the no-config case.
const prettierOptions = (await resolveConfig(outFile)) ?? {};
const formatted = await format(file, {
  ...prettierOptions,
  parser: 'typescript',
  filepath: outFile,
});
writeFileSync(outFile, formatted);
process.stdout.write(
  `wrote ${outFile} (${String(migrations.length)} migrations: ${migrations.map((m) => m.tag).join(', ')})\n`,
);
